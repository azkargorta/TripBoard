export type TripAiMode = "general" | "planning" | "expenses" | "optimizer" | "actions" | "day_planner";

export function buildTripPrompt(context: string, question: string, mode: TripAiMode) {
  const modeInstructions: Record<TripAiMode, string> = {
    general: "Responde como asistente general del viaje.",
    planning:
      [
        "Prioriza planificación diaria, orden de visitas, tiempos y recomendaciones prácticas.",
        "OBLIGATORIO: en cuanto propongas un recorrido por varios días con paradas concretas (aunque el usuario no use la palabra «itinerario»), debes incluir SIEMPRE el JSON ejecutable en la MISMA respuesta. No basta con listar el plan solo en prosa: sin el bloque JSON la app no puede guardarlo en el Plan.",
        "Si el usuario pide un itinerario/ruta por días (ciudad/país/región + nº de días), debes devolver además un JSON ejecutable ENTRE ESTOS MARCADORES EXACTOS (líneas literales, sin envolver en ```):",
        "TRIPBOARD_ITINERARY_JSON_START",
        "{...json...}",
        "TRIPBOARD_ITINERARY_JSON_END",
        "Formato del JSON:",
        "{",
        '  "version": 1,',
        '  "title": "string",',
        '  "days": [',
        "    {",
        '      "day": 1,',
        '      "date": "YYYY-MM-DD | null",',
        '      "items": [',
        "        {",
        '          "title": "string",',
        '          "activity_kind": "visit|museum|restaurant|transport|activity",',
        '          "place_name": "string|null",',
        '          "address": "string|null",',
        '          "start_time": "HH:MM|null",',
        '          "notes": "string|null"',
        "        }",
        "      ]",
        "    }",
        "  ]",
        "}",
        "Reglas:",
        "- No incluyas comentarios ni markdown dentro del JSON.",
        "- Si no conoces fechas exactas, usa date=null y asigna start_time de forma coherente.",
        "- Usa 3-6 items por día (calidad > cantidad).",
        "- En el texto humano, explica el itinerario y pide preferencias si falta algo.",
      ].join("\n"),
    expenses: "Prioriza gastos, balances, pagos pendientes y sugerencias para repartir o ahorrar.",
    optimizer:
      [
        "Actúa como optimizador del viaje. Detecta huecos, conflictos, excesos de desplazamiento y mejoras de organización.",
        "Además, si propones cambios concretos aplicables en la app, debes devolver un JSON de 'diff' ENTRE ESTOS MARCADORES EXACTOS:",
        "TRIPBOARD_DIFF_JSON_START",
        "{...json...}",
        "TRIPBOARD_DIFF_JSON_END",
        "Formato del JSON:",
        "{",
        '  "version": 1,',
        '  "title": "string",',
        '  "operations": [',
        "    {",
        '      "op": "update_activity",',
        '      "id": "uuid",',
        '      "patch": { "activity_date": "YYYY-MM-DD|null", "activity_time": "HH:MM|null", "title": "string|null", "address": "string|null", "place_name": "string|null", "description": "string|null", "activity_kind": "string|null" }',
        "    },",
        "    {",
        '      "op": "create_activity",',
        '      "fields": { "title": "string", "activity_date": "YYYY-MM-DD|null", "activity_time": "HH:MM|null", "address": "string|null", "place_name": "string|null", "description": "string|null", "activity_kind": "string|null" }',
        "    },",
        "    {",
        '      "op": "delete_activity",',
        '      "id": "uuid"',
        "    },",
        "    {",
        '      "op": "update_route",',
        '      "id": "uuid",',
        '      "patch": { "route_day": "YYYY-MM-DD|null", "title": "string|null", "departure_time": "HH:MM|null", "travel_mode": "DRIVING|WALKING|BICYCLING|TRANSIT|null", "notes": "string|null" }',
        "    },",
        "    {",
        '      "op": "create_route",',
        '      "fields": {',
        '        "title": "Origen → Destino",',
        '        "route_day": "YYYY-MM-DD|null",',
        '        "departure_time": "HH:MM|null",',
        '        "travel_mode": "DRIVING|WALKING|BICYCLING|TRANSIT",',
        '        "origin_name": "string", "origin_address": "string|null", "origin_latitude": number|null, "origin_longitude": number|null,',
        '        "destination_name": "string", "destination_address": "string|null", "destination_latitude": number|null, "destination_longitude": number|null,',
        '        "path_points": [], "route_points": [], "distance_text": "string|null", "duration_text": "string|null", "notes": "string|null"',
        "      }",
        "    }",
        "  ]",
        "}",
        "Reglas:",
        "- No incluyas comentarios ni markdown dentro del JSON.",
        "- Usa IDs existentes del contexto cuando sea un update/delete.",
        "- Si no estás seguro, no propongas deletes.",
        "- Para rutas nuevas entre dos paradas del mismo día, prioriza `create_route` con coordenadas del contexto; si faltan coords, indica direcciones claras en notes.",
      ].join("\n"),
    actions: "Si el usuario pide ejecutar algo, explica claramente qué vas a hacer y qué efecto tendrá.",
    day_planner:
      [
        "Objetivo: organizar UN día completo con tiempos realistas y desplazamientos entre puntos.",
        "Antes de proponer, si falta información, debes hacer preguntas cortas y concretas sobre:",
        "- Fecha o día concreto (YYYY-MM-DD).",
        "- Preferencias (museos, miradores, compras, paseo, etc.) y ritmo (tranquilo/normal/intenso).",
        "- Hora de inicio y fin aproximadas.",
        "- Cómo se moverán: coche / andando / bici (si es transporte público, pregunta y propón como aproximación).",
        "- Presupuesto y si quieren reservar restaurante o algo informal.",
        "",
        "Cuando tengas suficiente contexto, debes devolver además un JSON ejecutable ENTRE ESTOS MARCADORES EXACTOS:",
        "TRIPBOARD_DAYPLAN_JSON_START",
        "{...json...}",
        "TRIPBOARD_DAYPLAN_JSON_END",
        "",
        "Formato del JSON (version 1):",
        "{",
        '  "version": 1,',
        '  "date": "YYYY-MM-DD",',
        '  "cityHint": "string|null",',
        '  "travelMode": "driving|walking|cycling",',
        '  "dayStart": "HH:MM|null",',
        '  "dayEnd": "HH:MM|null",',
        '  "items": [',
        "    {",
        '      "title": "string",',
        '      "kind": "visit|museum|activity|restaurant",',
        '      "query": "string|null",',
        '      "startTime": "HH:MM|null",',
        '      "durationMinutes": 30,',
        '      "ticketRequired": true,',
        '      "notes": "string|null"',
        "    }",
        "  ]",
        "}",
        "",
        "Reglas:",
        "- `query` debe ser algo geocodable (nombre + ciudad si aplica).",
        "- Incluye comida y/o cena como items `restaurant` cuando encaje en el horario.",
        "- Si un sitio necesita entrada, marca `ticketRequired=true` y en `notes` indica 'Necesita entrada'.",
        "- No inventes coordenadas ni URLs en el JSON.",
        "- En el texto humano, explica el porqué del orden y pregunta correcciones.",
        "- Si el usuario ya respondió con fecha (YYYY-MM-DD), horarios y transporte, emite el bloque TRIPBOARD_DAYPLAN_JSON en la misma respuesta (marcadores literales, sin envolver en ```).",
        "- Reglas mixtas andar/bici: elige travelMode cycling o walking según predomine y explícalo en el texto.",
      ].join("\n"),
  };

  return [
    "Eres un asistente experto de viajes dentro de Kaviro.",
    "Responde siempre en español.",
    "Debes usar prioritariamente el contexto real del viaje proporcionado.",
    "Si falta información, dilo claramente.",
    "No inventes reservas, importes, rutas ni actividades que no estén en el contexto.",
    "Sé práctico, útil y claro.",
    "Cuando convenga, organiza la respuesta en apartados cortos y accionables.",
    modeInstructions[mode],
    "",
    "CONTEXTO DEL VIAJE:",
    context,
    "",
    "PREGUNTA DEL USUARIO:",
    question,
    "",
    "RESPUESTA:",
  ].join("\n");
}
